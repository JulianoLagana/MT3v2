"""
Script to plot .txt files generated by the Logger class.
"""

import matplotlib.pyplot as plt
from util.logger import load_data
from scipy.ndimage.filters import gaussian_filter1d


def load(path):
    return load_data(f'{path}', 'metrics/last_layer_losses')


trial1 = load('path_to_root/src/results/result_foldername')
trial2 = load('path_to_root/src/results/result_foldername2')


sigma = 0.0
trials = [trial1]
for trial in trials:
    if sigma > 0.00:
        trial[1] = gaussian_filter1d(trial[1], sigma=sigma)
    plt.plot(*trial)


plt.legend(['1', '2'])

plt.grid()
plt.show()
